#Спортсмен-лыжник начал тренировки, пробежав в первый день 10 км. Каждый следующий день он увеличивал длину пробега на P процентов от пробега
#предыдущего дня (P — вещественное, 0< P <50). По данному P определить, после какого дня суммарный пробег лыжника
#за все дни превысит 200 км, и вывести найденное количество дней K (целое) и суммарный пробег S (вещественное число).
try:
    P = float(input("Введите P (0 < P < 50): "))
    
    if P <= 0 or P >= 50:
        print("Ошибка: P должно быть в диапазоне от 0 до 50 (не включая границы)")
    else:
        day = 0
        total = 0.0
        current = 10.0  

        while total <= 200:
            day += 1
            total += current
            if total <= 200:
                current = current * (1 + P / 100)

        print("Количество дней K: ",day)
        print("Суммарный пробег S: ",total)
except ValueError:
    print("Ошибка: введите число!")
